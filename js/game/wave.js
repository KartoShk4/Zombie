/**
 * Переход на следующую волну
 * Усиливает всех существующих зомби
 */
function nextWave() {
    wave++;
    
    // Усиливаем всех зомби при переходе на новую волну
    for (let z of zombies) {
        z.speed += 0.1;   // Увеличиваем скорость
        z.health += 10;   // Увеличиваем здоровье
    }
}




