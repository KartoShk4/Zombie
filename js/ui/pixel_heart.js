/* ============================================
   ПИКСЕЛЬНОЕ СЕРДЦЕ
   ============================================
   Функция для отрисовки пиксельного сердца
   в интерфейсе игры.
   ============================================ */

/**
 * Отрисовка пиксельного сердца
 * @param {CanvasRenderingContext2D} ctx - Контекст canvas
 * @param {number} x - Позиция X
 * @param {number} y - Позиция Y
 * @param {number} scale - Масштаб (размер пикселя)
 */
function drawPixelHeart(ctx, x, y, scale = 3) {
    // Пиксельная карта сердца (1 = закрасить, 0 = пропустить)
    const pixels = [
        "01100110",  // Верхняя часть
        "11111111",  // Средняя часть
        "11111111",  // Средняя часть
        "01111110",  // Нижняя часть
        "00111100",  // Нижняя часть
        "00011000",  // Нижняя часть
        "00000000"   // Пустая строка
    ];

    ctx.fillStyle = "#ff3b3b";  // Красный цвет

    // Проходим по каждому пикселю
    for (let row = 0; row < pixels.length; row++) {
        for (let col = 0; col < pixels[row].length; col++) {
            if (pixels[row][col] === "1") {
                // Рисуем пиксель
                ctx.fillRect(
                    x + col * scale,
                    y + row * scale,
                    scale,
                    scale
                );
            }
        }
    }
}

